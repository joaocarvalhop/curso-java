package classe;

public class DesafioTrauma {
	
	int a = 3;
	static int b = 4;
	
	public static void main(String[] args) {
		
		// cria-se um atributo de instancia
		DesafioTrauma p = new DesafioTrauma();
		System.out.println(p.a);
		
		// ou só transforma a variável em static
		System.out.println(b);
		
	}

}
